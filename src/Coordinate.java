public class Coordinate {

	private int row;
	private int column;
	
	public Coordinate()
	{
		row = 0;
		column = 0;
	}
	
	public int getRow()
	{
		return row;
	}
	
	public int getColumn()
	{
		return column;
	}
	
	public void setRow(int row)
	{
		this.row = row;
	}
	
	public void setColumn(int column)
	{
		this.column = column;
	}
}
